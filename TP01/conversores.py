def celsius_para_fahrenheit(celsius):
    return celsius * 9/5 + 32

def fahrenheit_para_celsius(fahrenheit):
    return (fahrenheit - 32) * 5/9 

def metro_para_pes (metro):
    return metro * 3.28084

def pes_para_metros(pes):
    return pes / 3.28084

def quilometro_para_milhas(quilometro):
    return quilometro / 1.60934

def dia_para_hora(dia):
    return dia * 24
